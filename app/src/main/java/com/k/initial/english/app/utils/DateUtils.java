    /**
     * 按默认格式的字符串距离今天的天数
     *
     * @param date 日期字符串
     * @return 按默认格式的字符串距离今天的天数
     */
    public static int countDays(String date) {
        long t = Calendar.getInstance().getTime().getTime();
        Calendar c = Calendar.getInstance();
        c.setTime(parse(date));
        long t1 = c.getTime().getTime();
        return (int) (t / 1000 - t1 / 1000) / 3600 / 24;
    }

    public static String countTimeBetween(String date) {
        long t = Calendar.getInstance().getTime().getTime();
        Calendar c = Calendar.getInstance();
        c.setTime(parse(date));
        long t1 = c.getTime().getTime();
        int between = (int) ((t - t1) / 1000);// 转化成秒
        int year = between / 3600 / 24 / 60;
        int day = between / 3600 / 24;
        int hours = between % (24 * 3600) / 3600;
        int minute = between % 3600 / 60;

        if (year != 0) {
            return date2Str(c.getTime(), FORMAT_YMD_CN);
        } else if (day != 0) {
            return day + "天前";
        } else if (hours != 0) {
            return hours + "小时前";
        } else if (minute != 0) {
            return minute + "分钟前";
        } else {
            if (60 >= between) {
                return "刚刚";
            } else {
                return date2Str(c.getTime(), FORMAT_YMD_CN);
            }
        }
    }

